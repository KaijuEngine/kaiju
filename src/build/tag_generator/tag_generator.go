package main

import (
	"fmt"
	"os"
	"path/filepath"
	"runtime"
	"unicode"
)

var availableTags = []string{
	"editor", "debug", "filedrop",
}

const tagSetFmt = `//go:build %s

/******************************************************************************/
/* %-75s*/
/******************************************************************************/
/* MIT License, Copyright (c) 2015-present Brent Farris, (John 4:13-14)       */
/******************************************************************************/

// Code generated by src/build/tag_generator; DO NOT EDIT.

package build

const %s = true
`

const tagNotFmt = `//go:build !%s

/******************************************************************************/
/* %-75s*/
/******************************************************************************/
/* MIT License, Copyright (c) 2015-present Brent Farris, (John 4:13-14)       */
/******************************************************************************/

// Code generated by src/build/tag_generator; DO NOT EDIT.

package build

const %s = false
`

func main() { createTagFiles() }

func generatorRoot() string {
	_, file, _, ok := runtime.Caller(0)
	if !ok {
		panic("unable to resolve generator path")
	}
	return filepath.Clean(filepath.Join(filepath.Dir(file), ".."))
}

func setFile(name string) string { return filepath.Join(generatorRoot(), fmt.Sprintf("z%s.go", name)) }
func notFile(name string) string {
	return filepath.Join(generatorRoot(), fmt.Sprintf("z%s_not.go", name))
}

func createTagFiles() {
	for i := range availableTags {
		set, err := os.Create(setFile(availableTags[i]))
		if err != nil {
			panic(err)
		}
		defer set.Close()
		not, err := os.Create(notFile(availableTags[i]))
		if err != nil {
			panic(err)
		}
		defer not.Close()
		title := []rune(availableTags[i])
		title[0] = unicode.ToUpper(title[0])
		set.WriteString(fmt.Sprintf(tagSetFmt, availableTags[i], "z"+availableTags[i]+".go", string(title)))
		not.WriteString(fmt.Sprintf(tagNotFmt, availableTags[i], "z"+availableTags[i]+"_not.go", string(title)))
	}
}
