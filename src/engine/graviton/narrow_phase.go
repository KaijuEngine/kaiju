func collidePrimitiveStaticMesh(primitive Shape, mesh *MeshCollision, meshTransform *matrix.Transform) (Contact, bool) {
	switch primitive.Type {
	case ShapeTypeSphere:
		return collideSphereMesh(Sphere(primitive), mesh, meshTransform)
	case ShapeTypeCapsule:
		return collideCapsuleMesh(Capsule(primitive), mesh, meshTransform)
	case ShapeTypeOOBB:
		return collideOOBBMesh(OOBB(primitive), mesh, meshTransform)
	default:
		return Contact{}, false
	}
}

func collidePrimitiveStaticTerrain(primitive Shape, terrain *TerrainCollision, terrainTransform *matrix.Transform) (Contact, bool) {
	if terrain == nil || !terrain.valid() {
		return Contact{}, false
	}
	queryBounds := terrainLocalQueryBounds(shapeWorldAABB(primitive), terrainTransform)
	switch primitive.Type {
	case ShapeTypeSphere:
		return collideSphereTerrain(Sphere(primitive), terrain, terrainTransform, queryBounds)
	case ShapeTypeCapsule:
		return collideCapsuleTerrain(Capsule(primitive), terrain, terrainTransform, queryBounds)
	case ShapeTypeOOBB:
		return collideOOBBTerrain(OOBB(primitive), terrain, terrainTransform, queryBounds)
	default:
		return Contact{}, false
	}
}

func collideSphereSphere(a, b Sphere) (Contact, bool) {
	ab := b.Center.Subtract(a.Center)
	distSq := ab.LengthSquared()
	radius := a.Radius + b.Radius
	if distSq > radius*radius {
		return Contact{}, false
	}
	normal := matrix.Vec3Right()
	dist := matrix.Float(0)
	if distSq > contactEpsilon*contactEpsilon {
		dist = matrix.Sqrt(distSq)
		normal = ab.Scale(1 / dist)
	}
	pointA := a.Center.Add(normal.Scale(a.Radius))
	pointB := b.Center.Subtract(normal.Scale(b.Radius))
	return newContact(pointA, pointB, normal, radius-dist), true
}

func collideSphereMesh(s Sphere, mesh *MeshCollision, meshTransform *matrix.Transform) (Contact, bool) {
	if mesh == nil || len(mesh.Triangles) == 0 {
		return Contact{}, false
	}
	radiusSq := s.Radius * s.Radius
	bestDistanceSq := matrix.Inf(1)
	var bestContact Contact
	found := false
	mesh.ForEachWorldTriangle(meshTransform, func(tri DetailedTriangle) bool {
		closest := closestPointOnTriangle(s.Center, tri.Points[0], tri.Points[1], tri.Points[2])
		delta := closest.Subtract(s.Center)
		distSq := delta.LengthSquared()
		if distSq > radiusSq || distSq >= bestDistanceSq {
			return true
		}
		normal := primitiveToTriangleNormal(s.Center, closest, tri)
		dist := matrix.Float(0)
		if distSq > contactEpsilon*contactEpsilon {
			dist = matrix.Sqrt(distSq)
			normal = delta.Scale(1 / dist)
		}
		pointA := s.Center.Add(normal.Scale(s.Radius))
		bestContact = newContact(pointA, closest, normal, s.Radius-dist)
		bestDistanceSq = distSq
		found = true
		return true
	})
	return bestContact, found
}

func collideSphereTerrain(s Sphere, terrain *TerrainCollision, terrainTransform *matrix.Transform, queryBounds AABB) (Contact, bool) {
	radiusSq := s.Radius * s.Radius
	bestDistanceSq := matrix.Inf(1)
	var bestContact Contact
	found := false
	forEachTerrainWorldTriangle(terrain, terrainTransform, queryBounds, func(tri DetailedTriangle) bool {
		closest := closestPointOnTriangle(s.Center, tri.Points[0], tri.Points[1], tri.Points[2])
		delta := closest.Subtract(s.Center)
		distSq := delta.LengthSquared()
		if distSq > radiusSq || distSq >= bestDistanceSq {
			return true
		}
		normal := primitiveToTriangleNormal(s.Center, closest, tri)
		dist := matrix.Float(0)
		if distSq > contactEpsilon*contactEpsilon {
			dist = matrix.Sqrt(distSq)
			normal = delta.Scale(1 / dist)
		}
		pointA := s.Center.Add(normal.Scale(s.Radius))
		bestContact = newContact(pointA, closest, normal, s.Radius-dist)
		bestDistanceSq = distSq
		found = true
		return true
	})
	return bestContact, found
}

func collideSphereAABB(s Sphere, b AABB) (Contact, bool) {
	min := b.Min()
	max := b.Max()
	closest := clampVec3(s.Center, min, max)
	delta := closest.Subtract(s.Center)
	distSq := delta.LengthSquared()
	if distSq > s.Radius*s.Radius {
		return Contact{}, false
	}
	if distSq > contactEpsilon*contactEpsilon {
		dist := matrix.Sqrt(distSq)
		normal := delta.Scale(1 / dist)
		pointA := s.Center.Add(normal.Scale(s.Radius))
		return newContact(pointA, closest, normal, s.Radius-dist), true
	}
	normal, faceDistance := closestAABBFaceNormal(s.Center, b)
	pointA := s.Center.Add(normal.Scale(s.Radius))
	pointB := s.Center.Add(normal.Scale(faceDistance))
	return newContact(pointA, pointB, normal, s.Radius+faceDistance), true
}

func collideSphereOOBB(s Sphere, b OOBB) (Contact, bool) {
	localCenter := b.Orientation.Transpose().MultiplyVec3(s.Center.Subtract(b.Center))
	localBox := NewAABB(matrix.Vec3Zero(), b.Extent)
	localSphere := NewSphere(localCenter, s.Radius)
	contact, ok := collideSphereAABB(localSphere, localBox)
	if !ok {
		return Contact{}, false
	}
	contact.PointA = b.Orientation.MultiplyVec3(contact.PointA).Add(b.Center)
	contact.PointB = b.Orientation.MultiplyVec3(contact.PointB).Add(b.Center)
	contact.Point = contact.PointA.Add(contact.PointB).Scale(0.5)
	contact.Normal = safeNormal(b.Orientation.MultiplyVec3(contact.Normal), fallbackNormal(s.Center, b.Center))
	return contact, true
}

func collideSphereCapsule(s Sphere, c Capsule) (Contact, bool) {
	a, b := capsuleSegment(c)
	closest := closestPointOnSegment(s.Center, a, b)
	virtual := Sphere{
		Center: closest,
		Radius: c.Radius,
		Type:   ShapeTypeSphere,
	}
	return collideSphereSphere(s, virtual)
}

func collideSphereCylinder(s Sphere, c Cylinder) (Contact, bool) {
	direction := safeNormal(c.Direction, matrix.Vec3Up())
	halfHeight := c.Height * 0.5
	diff := s.Center.Subtract(c.Center)
	height := matrix.Vec3Dot(diff, direction)
	radial := diff.Subtract(direction.Scale(height))
	radialLength := radial.Length()
	radialNormal := safeNormal(radial, direction.Orthogonal())
	absHeight := matrix.Abs(height)
	insideHeight := absHeight <= halfHeight
	insideRadius := radialLength <= c.Radius
	if insideHeight && insideRadius {
		sideExit := c.Radius - radialLength
		capExit := halfHeight - absHeight
		normal := radialNormal
		pointB := c.Center.Add(direction.Scale(height)).Add(radialNormal.Scale(c.Radius))
		exitDistance := sideExit
		if capExit <= sideExit {
			sign := matrix.Float(1)
			if height < 0 {
				sign = -1
			}
			normal = direction.Scale(sign)
			pointB = c.Center.Add(direction.Scale(sign * halfHeight)).Add(radial)
			exitDistance = capExit
		}
		pointA := s.Center.Add(normal.Scale(s.Radius))
		return newContact(pointA, pointB, normal, s.Radius+exitDistance), true
	}
	clampedHeight := klib.Clamp(height, -halfHeight, halfHeight)
	clampedRadius := radialLength
	if clampedRadius > c.Radius {
		clampedRadius = c.Radius
	}
	closest := c.Center.Add(direction.Scale(clampedHeight)).Add(radialNormal.Scale(clampedRadius))
	delta := closest.Subtract(s.Center)
	distanceSq := delta.LengthSquared()
	if distanceSq > s.Radius*s.Radius {
		return Contact{}, false
	}
	distance := matrix.Float(0)
	normal := fallbackNormal(s.Center, c.Center)
	if distanceSq > contactEpsilon*contactEpsilon {
		distance = matrix.Sqrt(distanceSq)
		normal = delta.Scale(1 / distance)
	}
	pointA := s.Center.Add(normal.Scale(s.Radius))
	return newContact(pointA, closest, normal, s.Radius-distance), true
}

func collideSphereCone(s Sphere, c Cone) (Contact, bool) {
	closest, distance, inside := closestPointOnConeSurface(s.Center, c)
	delta := closest.Subtract(s.Center)
	distanceSq := delta.LengthSquared()
	if !inside && distanceSq > s.Radius*s.Radius {
		return Contact{}, false
	}
	normal := safeNormal(delta, fallbackNormal(s.Center, c.Center))
	if !inside && distanceSq > contactEpsilon*contactEpsilon {
		distance = matrix.Sqrt(distanceSq)
	}
	penetration := s.Radius - distance
	if inside {
		penetration = s.Radius + distance
	}
	pointA := s.Center.Add(normal.Scale(s.Radius))
	return newContact(pointA, closest, normal, penetration), true
}

func collideCylinderOOBB(c Cylinder, b OOBB) (Contact, bool) {
	direction := safeNormal(c.Direction, matrix.Vec3Up())
	axes := [8]matrix.Vec3{}
	axisCount := 0
	addAxis := func(axis matrix.Vec3) {
		if axis.LengthSquared() <= contactEpsilon*contactEpsilon {
			return
		}
		axes[axisCount] = axis
		axisCount++
	}
	addAxis(direction)
	for i := 0; i < 3; i++ {
		boxAxis := b.Orientation.ColumnVector(i)
		addAxis(boxAxis)
		addAxis(matrix.Vec3Cross(direction, boxAxis))
	}
	centerDelta := b.Center.Subtract(c.Center)
	radialDelta := centerDelta.Subtract(direction.Scale(matrix.Vec3Dot(centerDelta, direction)))
	addAxis(radialDelta)
	normal := matrix.Vec3Right()
	minOverlap := matrix.Inf(1)
	for i := 0; i < axisCount; i++ {
		axis := safeNormal(axes[i], matrix.Vec3Right())
		cRadius := projectCylinderRadius(c, axis)
		bRadius := projectOOBBRadius(b, axis)
		centerDistance := matrix.Vec3Dot(centerDelta, axis)
		overlap := cRadius + bRadius - matrix.Abs(centerDistance)
		if overlap < 0 {
			return Contact{}, false
		}
		if overlap < minOverlap {
			minOverlap = overlap
			if centerDistance < 0 {
				axis = axis.Negative()
			}
			normal = axis
		}
	}
	pointA := supportCylinder(c, normal)
	pointB := supportOOBB(b, normal.Negative())
	return newContact(pointA, pointB, normal, minOverlap), true
}

func collideConeOOBB(c Cone, b OOBB) (Contact, bool) {
	direction := safeNormal(c.Direction, matrix.Vec3Up())
	axes := [9]matrix.Vec3{}
	axisCount := 0
	addAxis := func(axis matrix.Vec3) {
		if axis.LengthSquared() <= contactEpsilon*contactEpsilon {
			return
		}
		axes[axisCount] = axis
		axisCount++
	}
	addAxis(direction)
	centerDelta := b.Center.Subtract(c.Center)
	radialDelta := centerDelta.Subtract(direction.Scale(matrix.Vec3Dot(centerDelta, direction)))
	if radialDelta.LengthSquared() > contactEpsilon*contactEpsilon {
		sideAxis := radialDelta.Normal().Scale(c.Height).Subtract(direction.Scale(c.Radius))
		addAxis(sideAxis)
		addAxis(radialDelta)
	}
	for i := 0; i < 3; i++ {
		boxAxis := b.Orientation.ColumnVector(i)
		addAxis(boxAxis)
		addAxis(matrix.Vec3Cross(direction, boxAxis))
	}
	normal := matrix.Vec3Right()
	minOverlap := matrix.Inf(1)
	for i := 0; i < axisCount; i++ {
		axis := safeNormal(axes[i], matrix.Vec3Right())
		coneMin, coneMax := projectCone(c, axis)
		boxCenter := matrix.Vec3Dot(b.Center, axis)
		boxRadius := projectOOBBRadius(b, axis)
		boxMin := boxCenter - boxRadius
		boxMax := boxCenter + boxRadius
		overlap := min(coneMax, boxMax) - max(coneMin, boxMin)
		if overlap < 0 {
			return Contact{}, false
		}
		if overlap < minOverlap {
			minOverlap = overlap
			if matrix.Vec3Dot(centerDelta, axis) < 0 {
				axis = axis.Negative()
			}
			normal = axis
		}
	}
	pointA := supportCone(c, normal)
	pointB := supportOOBB(b, normal.Negative())
	return newContact(pointA, pointB, normal, minOverlap), true
}

func collideCapsuleMesh(c Capsule, mesh *MeshCollision, meshTransform *matrix.Transform) (Contact, bool) {
	if mesh == nil || len(mesh.Triangles) == 0 {
		return Contact{}, false
	}
	a, b := capsuleSegment(c)
	radiusSq := c.Radius * c.Radius
	bestDistanceSq := matrix.Inf(1)
	var bestContact Contact
	found := false
	mesh.ForEachWorldTriangle(meshTransform, func(tri DetailedTriangle) bool {
		segPoint, triPoint := closestSegmentTriangle(a, b, tri.Points[0], tri.Points[1], tri.Points[2])
		delta := triPoint.Subtract(segPoint)
		distSq := delta.LengthSquared()
		if distSq > radiusSq || distSq >= bestDistanceSq {
			return true
		}
		normal := primitiveToTriangleNormal(segPoint, triPoint, tri)
		dist := matrix.Float(0)
		if distSq > contactEpsilon*contactEpsilon {
			dist = matrix.Sqrt(distSq)
			normal = delta.Scale(1 / dist)
		}
		pointA := segPoint.Add(normal.Scale(c.Radius))
		bestContact = newContact(pointA, triPoint, normal, c.Radius-dist)
		bestDistanceSq = distSq
		found = true
		return true
	})
	return bestContact, found
}

