func collideAABBAABB(a, b AABB) (Contact, bool) {
	delta := b.Center.Subtract(a.Center)
	overlap := a.Extent.Add(b.Extent).Subtract(delta.Abs())
	if overlap.X() < 0 || overlap.Y() < 0 || overlap.Z() < 0 {
		return Contact{}, false
	}
	axis := overlap.LongestAxis()
	penetration := overlap[axis]
	if overlap.X() <= overlap.Y() && overlap.X() <= overlap.Z() {
		axis = matrix.Vx
		penetration = overlap.X()
	} else if overlap.Y() <= overlap.Z() {
		axis = matrix.Vy
		penetration = overlap.Y()
	}
	normal := matrix.Vec3Zero()
	normal[axis] = 1
	if delta[axis] < 0 {
		normal[axis] = -1
	}
	min := matrix.Vec3Max(a.Min(), b.Min())
	max := matrix.Vec3Min(a.Max(), b.Max())
	point := min.Add(max).Scale(0.5)
	pointA := point
	pointB := point
	pointA[axis] = a.Center[axis] + normal[axis]*a.Extent[axis]
	pointB[axis] = b.Center[axis] - normal[axis]*b.Extent[axis]
	return newContact(pointA, pointB, normal, penetration), true
}

func collideOOBBOOBB(a, b OOBB) (Contact, bool) {
	normal := matrix.Vec3Right()
	minOverlap := matrix.Inf(1)
	centerDelta := b.Center.Subtract(a.Center)
	for i := 0; i < 3; i++ {
		if !satBoxAxis(a, b, a.Orientation.ColumnVector(i), centerDelta, &normal, &minOverlap) {
			return Contact{}, false
		}
		if !satBoxAxis(a, b, b.Orientation.ColumnVector(i), centerDelta, &normal, &minOverlap) {
			return Contact{}, false
		}
	}
	for i := 0; i < 3; i++ {
		for j := 0; j < 3; j++ {
			axis := matrix.Vec3Cross(a.Orientation.ColumnVector(i), b.Orientation.ColumnVector(j))
			if axis.LengthSquared() <= contactEpsilon*contactEpsilon {
				continue
			}
			if !satBoxAxis(a, b, axis, centerDelta, &normal, &minOverlap) {
				return Contact{}, false
			}
		}
	}
	pointA := supportOOBB(a, normal)
	pointB := supportOOBB(b, normal.Negative())
	return newContact(pointA, pointB, normal, minOverlap), true
}

func collideOOBBMesh(box OOBB, mesh *MeshCollision, meshTransform *matrix.Transform) (Contact, bool) {
	if mesh == nil || len(mesh.Triangles) == 0 {
		return Contact{}, false
	}
	bestPenetration := matrix.Inf(1)
	var bestContact Contact
	found := false
	mesh.ForEachWorldTriangle(meshTransform, func(tri DetailedTriangle) bool {
		contact, ok := collideOOBBTriangle(box, tri)
		if !ok || contact.Penetration >= bestPenetration {
			return true
		}
		bestPenetration = contact.Penetration
		bestContact = contact
		found = true
		return true
	})
	return bestContact, found
}

