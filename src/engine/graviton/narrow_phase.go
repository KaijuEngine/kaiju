const (
	contactEpsilon       matrix.Float = 0.00001
	narrowMinPairsPerJob              = 64
	maxManifoldContacts               = 4
)

// Contact contains one world-space contact point generated by the narrow phase.
// Normal always points from BodyA toward BodyB.
type Contact struct {
	BodyA       *RigidBody
	BodyB       *RigidBody
	Point       matrix.Vec3
	PointA      matrix.Vec3
	PointB      matrix.Vec3
	Normal      matrix.Vec3
	Penetration matrix.Float
}

// ContactManifold groups contacts for a colliding body pair.
type ContactManifold struct {
	BodyA    *RigidBody
	BodyB    *RigidBody
	Normal   matrix.Vec3
	Contacts [maxManifoldContacts]Contact
	Count    int
}

func (m *ContactManifold) add(contact Contact) {
	if m.Count >= len(m.Contacts) {
		return
	}
	m.Contacts[m.Count] = contact
	m.Count++
}

// NarrowPhase performs exact pair tests after the broad phase has produced
// candidate pairs. It owns reusable buffers so a physics step can run without
// per-pair allocations.
type NarrowPhase struct {
	manifolds []ContactManifold
	buffers   [][]ContactManifold
}

func (n *NarrowPhase) Reset() {
	n.manifolds = n.manifolds[:0]
	for i := range n.buffers {
		n.buffers[i] = n.buffers[i][:0]
	}
}

func (n *NarrowPhase) Manifolds() []ContactManifold {
	return n.manifolds
}

func (n *NarrowPhase) Collide(pairs []ActivePair, threads *concurrent.Threads) []ContactManifold {
	n.Reset()
	if len(pairs) == 0 {
		return n.manifolds
	}
	workers := broadPhaseWorkerCount(threads, len(pairs), narrowMinPairsPerJob)
	if workers == 1 {
		n.collideRange(pairs, 0, len(pairs), &n.manifolds)
		return n.manifolds
	}
	n.ensureBuffers(workers)
	runNarrowPhaseJobs(threads, workers, len(pairs), func(start, end, worker int) {
		buffer := n.buffers[worker][:0]
		n.collideRange(pairs, start, end, &buffer)
		n.buffers[worker] = buffer
	})
	total := 0
	for i := range workers {
		total += len(n.buffers[i])
	}
	if cap(n.manifolds) < total {
		n.manifolds = make([]ContactManifold, 0, total)
	}
	for i := range workers {
		n.manifolds = append(n.manifolds, n.buffers[i]...)
	}
	return n.manifolds
}

func (n *NarrowPhase) collideRange(pairs []ActivePair, start, end int, manifolds *[]ContactManifold) {
	for i := start; i < end; i++ {
		pair := pairs[i]
		if pair.BodyA == nil || pair.BodyB == nil {
			continue
		}
		manifold, ok := CollideBodies(pair.BodyA, pair.BodyB)
		if ok {
			*manifolds = append(*manifolds, manifold)
		}
	}
}

func (n *NarrowPhase) ensureBuffers(count int) {
	for len(n.buffers) < count {
		n.buffers = append(n.buffers, make([]ContactManifold, 0, 64))
	}
}

func runNarrowPhaseJobs(threads *concurrent.Threads, workers, items int, work func(start, end, worker int)) {
	jobs := make([]func(int), workers)
	wg := sync.WaitGroup{}
	wg.Add(workers)
	for worker := range workers {
		start := worker * items / workers
		end := (worker + 1) * items / workers
		worker := worker
		jobs[worker] = func(int) {
			defer wg.Done()
			work(start, end, worker)
		}
	}
	threads.AddWork(jobs)
	wg.Wait()
}

func CollideBodies(a, b *RigidBody) (ContactManifold, bool) {
	if a == nil || b == nil {
		return ContactManifold{}, false
	}
	if contact, ok := collideBodyMeshPair(a, b); ok {
		contact.BodyA = a
		contact.BodyB = b
		manifold := ContactManifold{
			BodyA:  a,
			BodyB:  b,
			Normal: contact.Normal,
		}
		manifold.add(contact)
		return manifold, true
	}
	if contact, ok := collideBodyTerrainPair(a, b); ok {
		contact.BodyA = a
		contact.BodyB = b
		manifold := ContactManifold{
			BodyA:  a,
			BodyB:  b,
			Normal: contact.Normal,
		}
		manifold.add(contact)
		return manifold, true
	}
	shapeA := worldShape(a)
	shapeB := worldShape(b)
	contact, ok := collideShapes(shapeA, shapeB)
	if !ok {
		return ContactManifold{}, false
	}
	contact.BodyA = a
	contact.BodyB = b
	manifold := ContactManifold{
		BodyA:  a,
		BodyB:  b,
		Normal: contact.Normal,
	}
	manifold.add(contact)
	return manifold, true
}

func collideBodyMeshPair(a, b *RigidBody) (Contact, bool) {
	if a.Collision.Shape.Type == ShapeTypeMesh || b.Collision.Shape.Type == ShapeTypeMesh {
		if a.Collision.Shape.Type == ShapeTypeMesh && b.Collision.Shape.Type == ShapeTypeMesh {
			return Contact{}, false
		}
		if a.Collision.Shape.Type == ShapeTypeMesh {
			if !a.IsStatic() || a.Collision.Mesh == nil {
				return Contact{}, false
			}
			contact, ok := collidePrimitiveStaticMesh(worldShape(b), a.Collision.Mesh, &a.Transform)
			return flipContact(contact), ok
		}
		if !b.IsStatic() || b.Collision.Mesh == nil {
			return Contact{}, false
		}
		return collidePrimitiveStaticMesh(worldShape(a), b.Collision.Mesh, &b.Transform)
	}
	return Contact{}, false
}

func collideBodyTerrainPair(a, b *RigidBody) (Contact, bool) {
	if a.Collision.Shape.Type == ShapeTypeTerrain || b.Collision.Shape.Type == ShapeTypeTerrain {
		if a.Collision.Shape.Type == ShapeTypeTerrain && b.Collision.Shape.Type == ShapeTypeTerrain {
			return Contact{}, false
		}
		if a.Collision.Shape.Type == ShapeTypeTerrain {
			if !a.IsStatic() || a.Collision.Terrain == nil {
				return Contact{}, false
			}
			contact, ok := collidePrimitiveStaticTerrain(worldShape(b), a.Collision.Terrain, &a.Transform)
			return flipContact(contact), ok
		}
		if !b.IsStatic() || b.Collision.Terrain == nil {
			return Contact{}, false
		}
		return collidePrimitiveStaticTerrain(worldShape(a), b.Collision.Terrain, &b.Transform)
	}
	return Contact{}, false
}

func collideShapes(a, b Shape) (Contact, bool) {
	switch a.Type {
	case ShapeTypeSphere:
		return collideSphereAny(Sphere(a), b)
	case ShapeTypeAABB:
		return collideAABBAny(AABB(a), b)
	case ShapeTypeOOBB:
		return collideOOBBAny(OOBB(a), b)
	case ShapeTypeCapsule:
		return collideCapsuleAny(Capsule(a), b)
	case ShapeTypeCylinder:
		return collideCylinderAny(Cylinder(a), b)
	case ShapeTypeCone:
		return collideConeAny(Cone(a), b)
	default:
		return Contact{}, false
	}
}

func collideSphereAny(a Sphere, b Shape) (Contact, bool) {
	switch b.Type {
	case ShapeTypeSphere:
		return collideSphereSphere(a, Sphere(b))
	case ShapeTypeAABB:
		return collideSphereAABB(a, AABB(b))
	case ShapeTypeOOBB:
		return collideSphereOOBB(a, OOBB(b))
	case ShapeTypeCapsule:
		c, ok := collideSphereCapsule(a, Capsule(b))
		return c, ok
	case ShapeTypeCylinder:
		return collideSphereCylinder(a, Cylinder(b))
	case ShapeTypeCone:
		return collideSphereCone(a, Cone(b))
	default:
		return Contact{}, false
	}
}

func collideAABBAny(a AABB, b Shape) (Contact, bool) {
	switch b.Type {
	case ShapeTypeSphere:
		c, ok := collideSphereAABB(Sphere(b), a)
		return flipContact(c), ok
	case ShapeTypeAABB:
		return collideAABBAABB(a, AABB(b))
	case ShapeTypeOOBB:
		return collideOOBBOOBB(OOBBFromAABB(a), OOBB(b))
	case ShapeTypeCapsule:
		c, ok := collideCapsuleAABB(Capsule(b), a)
		return flipContact(c), ok
	case ShapeTypeCylinder:
		c, ok := collideCylinderOOBB(Cylinder(b), OOBBFromAABB(a))
		return flipContact(c), ok
	case ShapeTypeCone:
		c, ok := collideConeOOBB(Cone(b), OOBBFromAABB(a))
		return flipContact(c), ok
	default:
		return Contact{}, false
	}
}

func collideOOBBAny(a OOBB, b Shape) (Contact, bool) {
	switch b.Type {
	case ShapeTypeSphere:
		c, ok := collideSphereOOBB(Sphere(b), a)
		return flipContact(c), ok
	case ShapeTypeAABB:
		c, ok := collideOOBBOOBB(a, OOBBFromAABB(AABB(b)))
		return c, ok
	case ShapeTypeOOBB:
		return collideOOBBOOBB(a, OOBB(b))
	case ShapeTypeCapsule:
		c, ok := collideCapsuleOOBB(Capsule(b), a)
		return flipContact(c), ok
	case ShapeTypeCylinder:
		c, ok := collideCylinderOOBB(Cylinder(b), a)
		return flipContact(c), ok
	case ShapeTypeCone:
		c, ok := collideConeOOBB(Cone(b), a)
		return flipContact(c), ok
	default:
		return Contact{}, false
	}
}

func collideCapsuleAny(a Capsule, b Shape) (Contact, bool) {
	switch b.Type {
	case ShapeTypeSphere:
		c, ok := collideSphereCapsule(Sphere(b), a)
		return flipContact(c), ok
	case ShapeTypeAABB:
		return collideCapsuleAABB(a, AABB(b))
	case ShapeTypeOOBB:
		return collideCapsuleOOBB(a, OOBB(b))
	case ShapeTypeCapsule:
		return collideCapsuleCapsule(a, Capsule(b))
	case ShapeTypeCylinder:
		if !a.IntersectsCylinder(Cylinder(b)) {
			return Contact{}, false
		}
	case ShapeTypeCone:
		if !a.IntersectsCone(Cone(b)) {
			return Contact{}, false
		}
	default:
		return Contact{}, false
	}
	return approximateContact(Shape(a), b), true
}

